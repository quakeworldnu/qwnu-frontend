<template>
    <div class="ml-3 mb-2 rounded-top rounded-bottom main-container">
        <div class="py-1 px-2 box-header rounded-top">News</div>
        <div class="p-2 pb-0 box-body rounded-bottom">
            <div v-for="article in articles" :key="article.id" class="p-2 box-content rounded-top rounded-bottom">
                <h1><a href="">{{article.title}}</a></h1>
                <span class="box-content-info">{{article.author.username}} / {{article.create_time}}</span>
                <div class="box-content-body" v-html="article.content"></div>
                <div class="box-content-footer">
                    <i class="fas fa-comments mr-2"></i>
                    <a href="">7</a>
                    <div class="float-right">
                        <i class="fas fa-pen mr-2"></i>
                        <i class="fas fa-trash"></i>
                    </div>
                </div>
            </div>
            <div class="bg-light p-2 rounded">
                <pagination :records="totalRecords" v-model="page" :per-page="pageSize" @paginate="onPageChange"></pagination>
            </div>
        </div>
    </div>
</template>

<script>
// @ is an alias to /src
import ArticleService from "@/services/ArticleService"
import XBBCODE from "@/lib/bbcode/xbbcode"

export default {
    name: "home",
    data: function() {
        return {
            articles: [],
            page: 1,
            pageSize: 1,
            totalRecords: 1
        }
    },
    mounted() {
        this.getArticles();
    },
    methods: {
        onPageChange() {
            this.getArticles();
        },
        getArticles() {
            ArticleService.getArticles(this.page, this.pageSize)
                .then(response => {
                    this.articles = response.data.data;
                    this.totalRecords = response.data.total;
                    this.pageSize = response.data.per_page;

                    for (let i = 0; i < this.articles.length; i++) {
                        let result = XBBCODE.process({
                            text: this.articles[i].content
                        })
                        this.articles[i].content = result.html;
                    }
                })
                .catch(error => {
                    console.log("Error: Could not fetch articles.", error);
                })
        }
    }
}
</script>
